// Tipos de Datos En JavaScript
// Tipo Number - Entero
let miEntero = 10;
console.log(miEntero);

// Tipo Float - Flotante o Decimal
let miFlotante = 10.5;
console.log(miFlotante);

// Tipo String - Cadena o Texto
let miString = 'Hola Mundo';
console.log(miString);

// Tipo Booleano - Verdadero o Falso
let miBooleano = true;
console.log(miBooleano);

// TIpo Null - Vacio
let miNull = null;
console.log(miNull);

// Tipo Undefined - Indefinido
let miUndefined = undefined;
console.log(miUndefined);


// Para Conocer que Tipo de Dato tenemos en Nuestra Variable Si es que Ocurre algun Error, etc. Utilizamos " TypeOf "  Para Saber el Tipo de Dato.
console.log(typeof miEntero);
console.log(typeof miBooleano);
console.log(typeof miFlotante);
console.log(typeof miString);
console.log(typeof miNull);
console.log(typeof miUndefined);


// Constantes --> Una Constante es un Valor que no Puede Ser Modificado cuando se Coloca el Nombre de la Constante debe ser Siempre en Mayuscula y Si tiene mas de Una Palabra se Coloca Guion Bajo (_).
const PI = 3.14;
const MI_CONSTANTE = 'Nombre Clave: Adrian';
console.log(PI);
console.log(MI_CONSTANTE);